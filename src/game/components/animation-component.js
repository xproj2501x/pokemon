/**
 * Animation Component
 * ===
 *
 * @module animationComponent
 */

////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import Component from '../../engine/component';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const KEYS = {
  fps: 'int',
  sequences: 'object',
  sequenceIndex: 'string',
  currentFrame: 'int',
  delta: 'int',
  sheet: 'string'
};

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * AnimationComponent
 * @class
 * @extends Component
 */
class AnimationComponent extends Component {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  get fps() {
    return this.state.fps;
  }

  get currentSequence() {
    return this.state.sequences[this.state.sequenceIndex];
  }

  get currentFrame() {
    return this.state.currentFrame;
  }

  get delta() {
    return this.state.delta;
  }

  /**
   * AnimationComponent
   * @constructor
   * @param {int} id - The identifier for the parent entity.
   * @param {object} state - The initial state of the component.
   */
  constructor(id, state) { // eslint-disable-line id-length
    super(id, KEYS, state);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////


  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method
   *
   * @static
   * @param {int} id - The identifier for the parent entity.
   * @param {object} state - The initial state of the component.
   *
   * @return {AnimationComponent} - A new health component instance.
   */
  static create(id, state) { // eslint-disable-line id-length
    return new AnimationComponent(id, state);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default AnimationComponent;
