/**
 * Menu Screen
 * ===
 *
 * @module menuScreen
 */

////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import Screen from '../../user-interface/screen';
import {KEYBOARD} from '../../engine/constants';
import {SCREEN} from './constants';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * MenuScreen
 * @class
 * @implements Screen
 */
class MenuScreen extends Screen {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * MenuScreen
   * @constructor
   */
  constructor() {
    super(SCREEN.MENU);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  run(event) {
    this._locked = true;
    switch (event) {
      case KEYBOARD.ENTER:
      case KEYBOARD.SPACE:
        this._nextState = SCREEN.PLAY;
        break;
      case KEYBOARD.KEY_H:
        break;
    }
  }

  enter() {
    const CONTAINER = document.getElementById('game');

    CONTAINER.innerHTML = `<i>MENU</i>`;
  }

  exit() {
    const CONTAINER = document.getElementById('game');

    this._nextState = null;
    this._locked = false;

    CONTAINER.innerHTML = ``;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method.
   *
   * @return {MenuScreen}
   */
  static create() {
    return new MenuScreen();
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default MenuScreen;
