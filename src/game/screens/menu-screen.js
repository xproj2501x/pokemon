/**
 * Menu Screen
 * ===
 *
 * @module menuScreen
 */

////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import Screen from '../../user-interface/screen';
import {KEYBOARD} from '../../engine/constants';
import {SCREEN} from './constants';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * MenuScreen
 * @class
 * @implements Screen
 */
class MenuScreen extends Screen {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   * @type {number}
   */
  _selectedCharacter;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * MenuScreen
   * @constructor
   */
  constructor() {
    super(SCREEN.MENU);
    this._selectedCharacter = 0;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  handleInput(event) {
    switch (event) {
      case KEYBOARD.UP_ARROW:
      case KEYBOARD.KEY_W:
        break;
      case KEYBOARD.DOWN_ARROW:
      case KEYBOARD.KEY_S:
        break;
      case KEYBOARD.ENTER:
        // Select character
        break;
      case KEYBOARD.KEY_N:
        // New character
        break;
      case KEYBOARD.KEY_D:
        // Delete character
        break;
    }
  }

  enter() {
    const CONTAINER = document.getElementById('game');

    CONTAINER.innerHTML = `<i>MENU</i>`;
  }

  exit() {
    const CONTAINER = document.getElementById('game');

    this._nextState = null;
    this._locked = false;

    CONTAINER.innerHTML = ``;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method.
   *
   * @return {MenuScreen}
   */
  static create() {
    return new MenuScreen();
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default MenuScreen;
