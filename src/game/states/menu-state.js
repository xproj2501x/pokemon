////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import State from '../../engine/state';
import {KEYBOARD} from '../../engine/constants';
import {STATE} from './constants';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * MenuState
 * @class
 * @implements State
 */
class MenuState extends State {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * MenuState
   * @constructor
   */
  constructor() {
    super(STATE.MENU);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  handleInput(input) {
    this._locked = true;
    switch (input) {
      case KEYBOARD.ENTER:
      case KEYBOARD.SPACE:
        this._nextState = STATE.PLAY;
        break;
      case KEYBOARD.KEY_H:
        break;
    }
    this._locked = false;
  }

  enter() {

  }

  exit() {
    this._nextState = null;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default MenuState;
