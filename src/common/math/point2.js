/**
 * Point2
 * @class
 */
class Point2 {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * The x coordinate for the vector.
   * @protected
   * @type {number}
   */
  _x;

  /**
   * The y coordinate for the vector.
   * @protected
   * @type {number}
   */
  _y;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * The x coordinate for the vector.
   * @readonly
   * @return {number}
   */
  get x() {
    return this._x;
  }

  /**
   * The y coordinate for the vector.
   * @readonly
   * @return {number}
   */
  get y() {
    return this._y;
  }

  /**
   * Point2
   * @constructor
   * @param {number} x - The x coordinate for the vector.
   * @param {number} y - The y coordinate for the vector.
   */
  constructor(x, y) {
    this._x = x;
    this._y = y;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Creates a new copy of the vector.
   * @return {Point2}
   */
  copy() {
    return Point2.create(this._x, this._y);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method
   * @static
   * @param {number} x - The x coordinate for the vector.
   * @param {number} y - The y coordinate for the vector.
   * @return {Point2}
   */
  static create(x, y) {
    return new Point2(x, y);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Point2;
