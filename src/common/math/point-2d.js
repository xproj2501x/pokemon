/**
 * Point2D
 * @class
 */
class Point2D {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  _x;
  _y;
  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  get x() {
    return this._x;
  }

  get y() {
    return this._y;
  }

  /**
   * Point2D
   * @constructor
   */
  constructor(x, y) {
    this._x = x;
    this._y = y;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method
   * @static
   * @return {Point2D}
   */
  static create() {
    return new Point2D();
  }
}

