const KEYS = {
  ATTACK: 0x00,
  DEFENSE: 0x01,
  SP_ATTACK: 0x02,
  SP_DEFENSE: 0x03,
  SPEED: 0x04
};

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * Stats
 * @class
 */
class Stats {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   * @type {DataModel}
   */
  _data;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @readonly
   * @return {int}
   */
  get attack() {
    return this._data.getProperty(KEYS.ATTACK);
  }

  /**
   * @readonly
   * @return {int}
   */
  get defense() {
    return this._data.getProperty(KEYS.DEFENSE);
  }

  /**
   * @readonly
   * @return {int}
   */
  get specialAttack() {
    return this._data.getProperty(KEYS.SP_ATTACK);
  }

  /**
   * @readonly
   * @return {int}
   */
  get specialDefense() {
    return this._data.getProperty(KEYS.SP_DEFENSE);
  }

  /**
   * @readonly
   * @return {int}
   */
  get speed() {
    return this._data.getProperty(KEYS.SPEED);
  }

  /**
   * @constructor
   * @param data
   */
  constructor(data) {
    this._data = data;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method
   */
  static create() {

  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Stats;
