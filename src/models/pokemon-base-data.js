import Stats from './stats';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const KEYS = {
  BASE_STATS: 0x00,
  TYPE_1: 0x01,
  TYPE_2: 0x02,
  CATCH_RATE: 0x03,
  XP_YIELD: 0x04,
  EFFORT_YIELDS: 0x05,
  ITEM_1: 0x06,
  ITEM_2: 0x07,
  GENDER: 0x08,
  EGG_CYCLES: 0x09,
  BASE_FRIENDSHIP: 0x0A,
  LEVEL_UP_TYPE: 0x0B,
  EGG_GROUP_1: 0x0C,
  EGG_GROUP_2: 0x0D,
  ABILITY_1: 0x0E,
  ABILITY_2: 0x0F,
  ABILITY_HIDDEN: 0x10,
  COLOR: 0x11
};

////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
/**
 * PokemonBaseData
 * @class
 */
class PokemonBaseData {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  _index;

  /**
   * @private
   * @type {DataModel}
   */
  _data;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @readonly
   * @return {Stats}
   */
  get baseStatistics() {
    return Stats.create(this._data[KEYS.BASE_STATS]);
  }

  /**
   * @readonly
   * @return {Stats}
   */
  get effortYields() {
    return Stats.create(this._data[KEYS.EFFORT_YIELDS]);
  }

  /**
   * @constructor
   * @param {DataModel} data -
   */
  constructor(data) {
    this._data = data;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Static factory method
   * @static
   * @return {PokemonBaseData}
   */
  static create() {

  }
}

