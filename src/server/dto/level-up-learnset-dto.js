////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import mongoose from 'mongoose';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const LevelUpLearnsetSchema = new mongoose.Schema({
  id: {
    type: Number,
    required: true,
  },
  moves: [
    {
      moveId: {
        type: Number,
        required: true
      },
      level: {
        type: Number,
        required: true
      }
    }
  ]
});

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default mongoose.model('LevelUpLearnsetDto', LevelUpLearnsetSchema);
