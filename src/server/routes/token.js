////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import {Router} from 'express';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const TOKEN_ROUTER = Router();

TOKEN_ROUTER.route('/')
  .post((req, res) => {
    res.json({data: '/token POST'});
  });

TOKEN_ROUTER.route('/:token')
  .get((req, res) => {
    res.json({data: '/token/:token GET'});
  });

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default TOKEN_ROUTER;
