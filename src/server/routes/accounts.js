////////////////////////////////////////////////////////////////////////////////
// Imports
////////////////////////////////////////////////////////////////////////////////
import {Router} from 'express';

////////////////////////////////////////////////////////////////////////////////
// Definitions
////////////////////////////////////////////////////////////////////////////////
const ACCOUNTS_ROUTER = Router();

ACCOUNTS_ROUTER.route('/')
  .post((req, res) => {
    res.json({data: '/accounts POST'});
  });

ACCOUNTS_ROUTER.route('/:id')
  .get((req, res) => {
    res.json({data: '/accounts/:id GET'});
  })
  .put((req, res) => {
    res.json({data: '/accounts/:id PUT'});
  })
  .delete((req, res) => {
    res.json({data: '/accounts/:id DELETE'});
  });

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default ACCOUNTS_ROUTER;
