/**
 * StateManager
 * @class
 */
class StateManager {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   * @type {object}
   */
  _states;

  /**
   * @private
   * @type {State}
   */
  _currentState;

  /**
   * @private
   * @type {string}
   */
  _previousState;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * StateManager
   * @constructor
   */
  constructor() {

  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  update(input) {
    this._currentState.update(input);
    if (this._currentState.nextState) {
      const NEXT_STATE = this._states[this._currentState.nextState];

      this._changeState(NEXT_STATE);
    }
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   *
   * @param {State} nextState
   * @private
   */
  _changeState(nextState) {
    this._previousState = this._currentState.name;
    this._currentState = nextState;
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default StateManager;
