/**
 * SystemManager
 * @class
 */
class SystemManager {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   * @type {Logger}
   */
  _logger;

  /**
   * @private
   * @type {MessageService}
   */
  _messageService;

  /**
   * Collection of systems registered for the simulation.
   * @private
   * @type {Array}
   */
  _systems;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * SystemManager
   * @constructor
   * @param {LogService} logService
   * @param {MessageService} messageService
   * @param {Array} systems
   */
  constructor(logService, messageService, systems) {
    this._logger = logService.register(this.constructor.name);
    this._messageService = messageService;
    this._systems = [];
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Calls the update method for each registered system.
   * @public
   */
  update() {
    this._systems.forEach((system) => {
      system.update();
    });
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////

  static create(logService, messageService, systems) {
    return new SystemManager(logService, messageService, systems);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default SystemManager;
