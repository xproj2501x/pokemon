/**
 * SystemManager
 * @class
 */
class SystemManager {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Collection of registered systems
   * @private
   * @type {object}
   */
  _systems;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  /**
   * SystemManager
   * @constructor
   */
  constructor() {
    this._systems = {};
  }

  //////////////////////////////////////////////////////////////////////////////
  // Public Methods
  //////////////////////////////////////////////////////////////////////////////
  /**
   * Runs update for each registered system
   * @param { object } state - the current state of the engine
   */
  update(state) {
    for (const KEY in this._systems) {
      if (this._systems.hasOwnProperty(KEY)) {
        const SYSTEM = this._systems[KEY];

        SYSTEM.update(state);
      }
    }
  }

  //////////////////////////////////////////////////////////////////////////////
  // Private Methods
  //////////////////////////////////////////////////////////////////////////////

  /**
   * Instantiates a system and adds it to the collection
   * @private
   * @param { object } template - a template for the system to be added
   * @return { boolean }
   */
  _addSystem(template) {
    if (!this._hasSystem(template.id)) {
      const SYSTEM = new template.constructor();

      this._systems[template.id] = SYSTEM;
      return true;
    }
    return false;
  }

  /**
   * Instantiates a system and adds it to the collection
   * @private
   * @param { string } name - the name of the system
   * @return { boolean }
   */
  _hasSystem(name) {
    return name in this._systems;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Static Methods
  //////////////////////////////////////////////////////////////////////////////

  static create() {
    return new SystemManager();
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default SystemManager;
